function readMoreBtn1(){
    let more1 = document.getElementById("more1");
    let btnReadMore1 = document.getElementById("readMoreBtn1");

    if(more1.style.display === "inline"){
        btnReadMore1.innerHTML = "читати далі...";
        more1.style.display="none";
    } else {
        btnReadMore1.innerHTML = "приховати";
        more1.style.display="inline";
    }
}
