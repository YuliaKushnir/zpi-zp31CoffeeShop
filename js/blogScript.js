function readMoreBtn1(){
    const more1 = document.getElementById("more1");
    const btnReadMore1 = document.getElementById("readMoreBtn1");

    if(more1.style.display === "inline"){
        btnReadMore1.textContent = "читати далі...";
        more1.style.display="none";
    } else {
        btnReadMore1.textContent = "приховати";
        more1.style.display="inline";
    }
}
